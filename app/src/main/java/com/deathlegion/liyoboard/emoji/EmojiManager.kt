            return instance ?: EmojiManager(context.applicationContext).also {
                instance = it
                it.loadRecentEmojis()
            }
        }
    }

    private fun loadRecentEmojis() {
        val json = prefs.getString("recent_emojis", null)
        json?.let {
            val type = object : TypeToken<List<String>>() {}.type
            recentEmojis = gson.fromJson(it, type)
        }
    }

    private fun saveRecentEmojis() {
        prefs.edit().putString("recent_emojis", gson.toJson(recentEmojis)).apply()
    }

    /**
     * Get emojis for a specific category
     */
    fun getEmojis(category: EmojiCategory): List<String> {
        return if (category == EmojiCategory.RECENT) {
            recentEmojis.toList()
        } else {
            emojiDatabase[category] ?: emptyList()
        }
    }

    /**
     * Add an emoji to recent list
     */
    fun addToRecent(emoji: String) {
        recentEmojis.remove(emoji)
        recentEmojis.add(0, emoji)
        if (recentEmojis.size > maxRecent) {
            recentEmojis = recentEmojis.take(maxRecent).toMutableList()
        }
        saveRecentEmojis()
    }

    /**
     * Get recent emojis
     */
    fun getRecentEmojis(): List<String> = recentEmojis.toList()

    /**
     * Clear recent emojis
     */
    fun clearRecent() {
        recentEmojis.clear()
        saveRecentEmojis()
    }

    /**
     * Search emojis by keyword
     */
    fun searchEmojis(query: String): List<String> {
        if (query.isBlank()) return emptyList()
        // Basic search - in production, would have emoji name mappings
        return emojiDatabase.values.flatten().filter {
            // Simple character match for now
            it.contains(query)
        }
    }

    /**
     * Get all categories
     */
    fun getCategories(): List<EmojiCategory> = EmojiCategory.entries
